    @Test(dataProvider = "orbisID", groups = {"regression", "entity_issuer"})
    @Xray(test = {4049,5868, 5869})
    public void validateSubSectorDrivers(@Optional String orbisID) {
        SoftAssert softAssert = new SoftAssert();
        String sector = EntityPageQueries.getSectorFromOrbisId(orbisID);
        System.out.println("Sector:" + sector);

        List<SectorDriverDBModel> sectorDriversFromDB = EntityPageQueries.getSectorDrivers(sector);
        System.out.println(sectorDriversFromDB.size());
        List<SectorDriversWrapper> sectorDriversList = Arrays.asList(
                controller.getSectorDrivers(orbisID)
                        .as(SectorDriversWrapper[].class));
        softAssert.assertEquals(sectorDriversList.get(0).getDriver_details().size(), 3, "3 Driver Group Verified");

        for (SectorIndicator c : sectorDriversList.get(0).getDriver_details()) {

            String indicator = c.getIndicator();
            for (SectorDrivers sectorDriver : c.getDrivers()) {
                String category = "";
                switch (sectorDriver.getCriteria_weight_value()) {
                    case 0:
                        category = "Low";
                        break;
                    case 1:
                        category = "Moderate";
                        break;
                    case 2:
                        category = "High";
                        break;
                    case 3:
                        category = "Very High";
                        break;
                }

                softAssert.assertEquals(sectorDriver.getCriteria_weight(), category, "Weight Category Matched");
            }

            List<SectorDriverDBModel> driversInSectorIndicator = sectorDriversFromDB.stream().
                    filter(p -> p.getINDICATOR().equals(c.getIndicator())).collect(Collectors.toList());


            softAssert.assertEquals(driversInSectorIndicator.size(), c.getDrivers().size(), "Drivers in " + c.getIndicator() + "group matched");

            for (SectorDrivers driver : c.getDrivers()) {
                String criteriaID = driver.getCriteria_id();
                String actualName = driver.getDomain() + " / " + driver.getCriteria_name();

                SectorDriverDBModel expectedDriver = driversInSectorIndicator.stream().filter(p -> p.getCRITERIA().equals(criteriaID)
                        && p.getNAME().equals(actualName)
                ).findFirst().get();

                softAssert.assertEquals(c.getIndicator(), expectedDriver.getINDICATOR(), c.getIndicator() + "Drivers Indicator Matching");
                softAssert.assertEquals(actualName, expectedDriver.getNAME(), "Driver name matching");
                softAssert.assertEquals(driver.getCriteria_weight_value(), expectedDriver.getVALUE(), "Weight Value matching");
                softAssert.assertEquals(driver.getCriteria_weight(), expectedDriver.getCritteria_weight(), "Validate Weight Text");

            }

        }

        softAssert.assertAll();

    }

